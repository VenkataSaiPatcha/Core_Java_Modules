package InterviewConcept;

public class DemoClass 
{
	private int a;
	private int b;
	
	public DemoClass(int a, int b)
	{
		this.a=a;
		this.b=b;
	}
	public DemoClass()
	{
		
	}
	public void setA(int a)
	{
		this.a = a;
	}
	public int getA()
	{
		return a;
	}
	
	public void setB(int b)
	{
		this.b = b;
	}
	public int getB()
	{
		return b;
	}
}
